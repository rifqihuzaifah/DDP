import math

def tambah(bil1, bil2):
    hasil = 7 + 3
    print("Hasil Tambah Dari",7, "+", 3, "=", hasil)

def kurang(bil1, bil2):
    hasil = 10 - 8
    print("Hasil Pengurangan Dari",10, "-", 8, "=", hasil)

def kali(bil1, bil2):
    hasil = 2 * 6
    print("Hasil Perkalian Dari",2, "*", 6, "=", hasil)

def bagi(bil1, bil2):
    hasil = 18 / 6
    print("Hasil Pembagian Dari",18, "/", 6, "=", hasil)

def pangkat(bil1, bil2):
    hasil = math.pow(7, 4)
    print("Hasil Pemangkatan Dari",7, "^", 4, "=", hasil)